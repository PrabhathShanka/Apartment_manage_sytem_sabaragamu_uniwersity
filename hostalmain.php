          <h1>How to Apply for our Hostels?</h1>
          <h5>
          Sabaragamuwa University of Sri Lanka offers hostel accommodation to its students to provide a comfortable and convenient living environment. The application process for hostel facilities is integrated into the overall registration procedure. Students interested in availing hostel accommodation must submit their application during this time, ensuring they meet the necessary eligibility criteria.

The Student Affairs Department oversees the hostel allocation process. To be eligible for hostel facilities, students must reside at a distance of more than 40 kilometers from the university. This requirement aims to accommodate those who face significant commuting challenges. Once a student is officially enrolled and meets the eligibility criteria, the university will allocate a hostel to them.<br>

The university's hostel allocation system is designed to facilitate a smooth transition to campus life for new students. By providing on-campus accommodation, the university aims to create a conducive environment for academic pursuits and social interactions.
